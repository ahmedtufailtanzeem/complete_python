# Simple print's with quote
print("\nSimple print with quotes")
print("Hello World in double quotes")
print('Hello World in single quotes')
print("Hello 'Tanzeem'")
print('Hello "Tanzeem Ahmed"')

# Escape sequence
print("\nEscape Sequence")
print("Hello \"Tanzeem\"")
print('Hello \'Tanzeem Ahmed\'')
print('Hello \nTanzeem Ahmed')
print('Hello Tanzeem\tAhmed')
print('Hello Tanzeem  \bAhmed #\b')

# Escape characters as Normal Text
print("Escape characters as normal text")
print("Hello \\nTanzeem Ahmed")
print("Hello \\tTanzeem Ahmed")
print("Hello with 4 backslash \\\\\\\\")
print("\\\" - \\\'")

# Exercise
print("\nExercise")
